Aristotle was big on finding a starting point. That sounds simple, but we live always in the middle of things, never fully able to step outside of circumstances. My current circumstances are that I'm at the ◊link["https://sevenstarsbakery.com/location/cranston/"]{Seven Stars Bakery} in Cranston, Rhode Island, a short drive from my house, most of the way through a coffee that I'll soon get refilled. The bakery is buzzing with chatter and espresso-making. I absolutely love cafe writing. The sounds around me that demand nothing from me create the perfect white noise for me to focus within. It's as good a starting point as any.

An essential property of any writing is that you have to be able to stop doing any other life things in that particular moment. You step as far outside of circumstances, at least those that you are an active participant in, as you can. You can't feed a baby and write at the same time, or call a contractor to get a bid to fix the gutters, or wash the dishes and also write. You can't think really, let alone actually put down words on the page. 

Writing shares this quality with meditation. You stop your other active living, you step aside, you dive in. Of course, you're not actually outside of life, but you are as much outside of it as you can be. And from there, you have a distinct perspective, one that allows for integrating old experiences and creating new ones. 

The barista has refilled my coffee and I've written a bit. Precisely what I came here to do. Prior to the actual writing, I completed a round of ◊emphasis{getting set up}, which I'll go over in the next chapter.



