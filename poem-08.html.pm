◊poem{In the car
Natalie sleeping
Drinking my coffee

The mall is for walking babies in strollers while it snows in flurries

For getting out of the house
On cold February days
with nothing but chores and entertainment to pass the time

I'll try on a fitted hat
and let Natalie get her first
glimpse of shopping for stuff that isn't food}
