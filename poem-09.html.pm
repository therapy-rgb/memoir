◊poem{morning time
and we all slept through the night
what a wonderful thing I took for granted
all those years without a baby

you only cried out once
then went back to sleep
I drank some water and did the same

I even have time to brush my teeth
before you wake up
ready for a bottle and a diaper change
to start seeing this world
new all over again}
